package basic;

public class SwitchCaseDemo {

	public static void main(String[] args) {
		
		int day;
		
		day =9;
		
		switch(day) {
		
		case 1:
			System.out.println("Happy Monday");
			break;
		case 2: 
			System.out.println("Happy Tuesday");
			break;
		case 3:
			System.out.println("Happy Wednesday");
			break;
		default:
			System.out.println("Wrong day");
				
		}

	}

}
