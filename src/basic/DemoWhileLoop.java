package basic;

public class DemoWhileLoop {

	// Variables -- name, data type
	// decision making - if .. else, if ... else if ... else, switch case
	// loop - for loop, while loop

	public static void main(String[] args) {

		int i = 0;

		while (i < 100) {
			System.out.println(i + ": Hello World");
			i++;
		}//end of while

	}//end of main

}//end of class
